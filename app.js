const FizzBuzz = (firstNumber, lastNumber, direction) => {
  if (typeof firstNumber !== "number") {
    return console.log("áƒ’áƒ—áƒ®áƒáƒ•áƒ— áƒ¨áƒ”áƒ˜áƒ§áƒ•áƒáƒœáƒ”áƒ— áƒ¡áƒáƒ¬áƒ§áƒ˜áƒ¡áƒ˜ áƒ áƒ˜áƒªáƒ®áƒ•áƒ˜");
  }
  if (typeof lastNumber !== "number") {
    return console.log("áƒ’áƒ—áƒ®áƒáƒ•áƒ— áƒ¨áƒ”áƒ˜áƒ§áƒ•áƒáƒœáƒáƒ— áƒ¡áƒáƒ‘áƒáƒšáƒáƒ áƒ áƒ˜áƒªáƒ®áƒ•áƒ˜");
  }
  if (direction != "-" && direction != "+") {
    return console.log("áƒ’áƒ—áƒ®áƒáƒ•áƒ— áƒ¨áƒ”áƒ˜áƒ§áƒ•áƒáƒœáƒáƒ— + áƒáƒœ -");
  }

  let numbers = [];
  for (var i = firstNumber; i < lastNumber; i++) {
    numbers.push(i);
  }

  direction === "-"
    ? numbers.sort((a, b) => b - a)
    : numbers.sort((a, b) => a - b);

  for (let i of numbers) {
    if (i % 3 === 0 && i % 5 === 0) {
      console.log(i + " fizzbuzz");
    } else if (i % 3 === 0) {
      console.log(i + " fizz");
    } else if (i % 5 === 0) {
      console.log(i + " buzz");
    } else {
      console.log(i);
    }
  }
};

